function I = inter_pt(X1,Y1,X2,Y2)

x1 = X1(1);
x2 = Y1(1);
x3 = X2(1);
x4 = Y2(1);
y1 = X1(2);
y2 = Y1(2);
y3 = X2(2);
y4 = Y2(2);
I = [((x1*y2-y1*x2)*(x3-x4)-(x1-x2)*(x3*y4-y3*x4))/((x1-x2)*(y3-y4)-(y1-y2)*(x3-x4)), ...
     ((x1*y2-y1*x2)*(y3-y4)-(y1-y2)*(x3*y4-y3*x4))/((x1-x2)*(y3-y4)-(y1-y2)*(x3-x4))];

end
